    private boolean verificarCampos(){
        if(txtNome.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Campo 'Nome' est√° v√°zio");
            return false;
        }
        if(!txtNome.getText().matches("^[\\p{L} ]+$")){
            JOptionPane.showMessageDialog(null, "Campo 'Nome' possui caracteres inv√°lidos");
            return false;
        }
        if(txtEmail.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Campo 'Email' est√° v√°zio");
            return false;
        }
        if(!txtEmail.getText().matches("^[a-zA-Z._]+@[a-zA-Z._]+.[a-zA-Z._]+$")){
            JOptionPane.showMessageDialog(null, "Campo 'Email' possui caracteres inv√°lidos");
            return false;
        }
        if(txtDataNasc.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Campo 'Data Nascimento' est√° v√°zio");
            return false;
        }
        if(!txtDataNasc.getText().matches("^[0-9]{2}/[0-9]{2}/[0-9]{4}$")){
            JOptionPane.showMessageDialog(null, "Campo 'Data Nascimento' possui caracteres inv√°lidos"
                    +"\n\nEx: 01/01/2000");
            return false;
        }
        char[] senha = txtSenha.getPassword();
        if(new String(senha).length() < 8){
            JOptionPane.showMessageDialog(null, "Campo 'Senha' deve ser maior que 8 caracteres");
            return false;
        }
        
        if(!new String(senha).equals(new String(txtSenha1.getPassword()))){
            JOptionPane.showMessageDialog(null, "As senhas n√£o condiz");
            return false;
        }

        return true;
    }
    
    private void btSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btSalvarMouseClicked
        if(!verificarCampos()){
            return;
        }

        UsuarioController controller = new UsuarioController();
        String senha = new String (txtSenha.getPassword());
        if(controller.adicionarUsuario(txtNome.getText(),txtEmail.getText(),
            senha, txtDataNasc.getText(), Utils.salvarBoolean(chkAtivo.isSelected()))){
        this.dispose();

        }
    }//GEN-LAST:event_btSalvarMouseClicked

